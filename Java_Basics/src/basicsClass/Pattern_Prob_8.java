package basicsClass;

public class Pattern_Prob_8 {
    public static void main(String[] args) {
        System.out.println("Question 1 - Print rectangular pattern?");
        System.out.println("Answer");
        for(int i=1; i<=3; i++){
            for (int j=1; j<=6; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
